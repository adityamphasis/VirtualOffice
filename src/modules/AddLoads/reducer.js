// @flow
import { Map } from 'immutable';
import {
  ADDLOAD_REQUEST,
  ADDLOAD_SUCCESS,
  ADDLOAD_FAILURE
} from './types';
import type State from './types';
import { setAuthenticationToken } from './actions';




const INITIAL_STATE = [{
  error: null,
  response: null,
  isBusy: false
}];



const reducer = (state: State = INITIAL_STATE, action) => {
  switch (action.type) {
    case ADDLOAD_REQUEST:
    return {
        ...state,
        isBusy: true,
        response: null
      };
      //return state.update('isBusy', () => true);
    case ADDLOAD_SUCCESS:
    return {
        ...state,
        isBusy: false,
        response: action.payload
      };


      case ADDLOAD_FAILURE:
      return {
          ...state,
          isBusy: false,
          response: null
        };
    default:
      return state;
  }
};

export default reducer;
